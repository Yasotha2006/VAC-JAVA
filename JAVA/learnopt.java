public class learnopt
{
public static void main(String args[])
{
int a=5;
System.out.println(a);
System.out.println("------------------------------------");
a=a+7;
a+=7;
System.out.println("The value of a"+a);
System.out.println("---------------------------------------");
int b=11;
int c=3;
System.out.println(b+c);//add
System.out.println("------------");
System.out.println(b-c);//sub
System.out.println("------------");
System.out.println(b*c);//multiply
System.out.println("------------");
System.out.println(b/c);//dividion
System.out.println("------------");
System.out.println(b%c);//mod
System.out.println("-------------");
System.out.println("---------------------------------------------");
int p=5;
int q=7;
System.out.println(p==q);
System.out.println(p!=q);
System.out.println(p<q);
System.out.println(p<=q);
System.out.println(p>q);
System.out.println(p>=q);
System.out.println("----------------------------------------------");
int s=20;
int m=15;
System.out.println(s>m&&s>=m);//and
System.out.println(s==m||s<m);//or
System.out.println("---------------------------------------------");
int k=7;
System.out.println(k++);
System.out.println(k);
System.out.println("-------------------------------");
System.out.println(++k);
System.out.println(k--);
System.out.println(k);
System.out.println("--------------------------");
System.out.println(--k);
}
}